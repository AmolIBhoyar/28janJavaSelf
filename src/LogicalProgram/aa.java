package LogicalProgram;

public class aa 
{
	public static void main(String[] args)
	{
		String s="amola";
		char c='a';
		int count=0;
		char[]ar=s.toCharArray();
		for(int i=0;i<=ar.length-1;i++)
		{
			if(ar[i]==c)
			{
				count++;
				
			}
			
		}
		System.out.println(count);
	}

}
